static void print_logo_narrow(void) {
    render_logo();
    oled_set_cursor(0, 3);

    oled_write_raw_P(kaonashi64, sizeof(kaonashi64));

    /* wpm counter */
    uint8_t n = current_wpm;
    char    wpm_str[4];
    oled_set_cursor(0, 14);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write(wpm_str, false);

    oled_set_cursor(0, 15);
    oled_write(" wpm", false);
    // oled_write_raw_P(crkbd_logo, sizeof(crkbd_logo));
}

static void print_status_narrow(void) {
    /* Print current mode */
    oled_set_cursor(0, 0);
    uint8_t host_os = detected_host_os();
    if (host_os == OS_MACOS || host_os == OS_IOS) {
        oled_write_raw_P(mac_logo, sizeof(mac_logo));
    } else if (host_os == OS_WINDOWS) {
        oled_write_raw_P(windows_logo, sizeof(windows_logo));
    } else if (host_os == OS_LINUX) {
        oled_write_raw_P(arch_logo, sizeof(arch_logo));
    } else if (host_os == OS_NINTENDO_SWITCH) {
        oled_write_raw_P(nintendo_logo, sizeof(nintendo_logo));
    } else if (host_os == OS_UNSURE) {
        oled_write_raw_P(question_mark, sizeof(question_mark));
    }

    oled_set_cursor(0, 3);

    switch (get_highest_layer(default_layer_state)) {
        case U_BASE:
            oled_write("QWRTY", false);
            break;
        case U_EXTRA:
            oled_write("COLMK", false);
            break;
        case U_TAP:
            oled_write("TAP", false);
            break;
        default:
            oled_write("UNDEF", false);
    }

    oled_set_cursor(0, 5);
    oled_write("LAYER", false);

    switch (get_highest_layer(layer_state)) {
        case U_BASE:
            oled_write_ln("Base", false);
            break;
        case U_NUM:
            oled_write_ln("Num", false);
            break;
        case U_NAV:
            oled_write_ln("Nav", false);
            break;
        case U_FUN:
            oled_write_ln("Fun", false);
            break;
        case U_SYM:
            oled_write_ln("Sym", false);
            break;
        case U_MEDIA:
            oled_write_ln("Media", false);
            break;
        case U_MOUSE:
            oled_write_ln("Mouse", false);
            break;
        case U_BUTTON:
            oled_write_ln("Btn", false);
            break;
        default:
            oled_write_ln("UNDEF", false);
    }

    // /* caps lock */
    oled_set_cursor(0, 8);
    oled_write("CPSLK", capsLockState);

    /* KEYBOARD PET RENDER START */

    render_luna(0, 13);

    /* KEYBOARD PET RENDER END */
}

